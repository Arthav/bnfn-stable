"use client";

import { useRef } from "react";
import { Button } from "@nextui-org/button";
import { Card, CardBody, CardHeader, CardFooter } from "@nextui-org/card";
import { Divider } from "@nextui-org/divider";
import { Link } from "@nextui-org/link";
import { BrandResult as BrandResultType } from "@/types/brand";
import { jsPDF } from "jspdf";
import autoTable from "jspdf-autotable";
import { DownloadIcon } from "lucide-react";

interface BrandResultProps {
    result: BrandResultType;
    onReset: () => void;
}

export function BrandResult({ result, onReset }: BrandResultProps) {
    const downloadPDF = () => {
        const doc = new jsPDF();
        const pageWidth = doc.internal.pageSize.getWidth();
        const pageHeight = doc.internal.pageSize.getHeight();

        // 1. Header with Business Name
        doc.setFontSize(24);
        doc.setTextColor(40, 40, 40);
        // Use business name if available, otherwise fallback
        const title = result.businessName || "Brand Bible";
        doc.text(title, pageWidth / 2, 20, { align: "center" });

        // 2. Business Description (if available)
        let yPos = 30;
        if (result.description) {
            doc.setFontSize(11);
            doc.setTextColor(80, 80, 80);
            const splitDesc = doc.splitTextToSize(result.description, pageWidth - 40);
            doc.text(splitDesc, pageWidth / 2, yPos, { align: "center" });
            yPos += splitDesc.length * 5 + 10;
        } else {
            yPos += 10;
        }

        // 3. Theme Concept
        doc.setFontSize(16);
        doc.setTextColor(0, 0, 0);
        doc.text("Theme Concept", 14, yPos);
        yPos += 8;

        doc.setFontSize(11);
        doc.setTextColor(60, 60, 60);
        const splitTheme = doc.splitTextToSize(result.themeDescription, pageWidth - 28);
        doc.text(splitTheme, 14, yPos);
        yPos += splitTheme.length * 6 + 10;

        // 4. Color Palette
        doc.setFontSize(16);
        doc.setTextColor(0, 0, 0);
        doc.text("Color Palette", 14, yPos);
        yPos += 8;

        const colors = [
            ["Primary", result.colorPalette.primary],
            ["Secondary", result.colorPalette.secondary],
            ["Background", result.colorPalette.background],
            ["Text", result.colorPalette.text],
            ["Tertiary", result.colorPalette.tertiary],
        ];

        autoTable(doc, {
            startY: yPos,
            head: [["Role", "Hex Code", "Preview"]],
            body: colors.map(c => [c[0], c[1], ""]),
            didParseCell: function (data) {
                if (data.column.index === 2 && data.section === 'body') {
                    const hex = data.row.raw[1] as string;
                    data.cell.styles.fillColor = hex;
                }
            }
        });

        // @ts-ignore
        yPos = doc.lastAutoTable.finalY + 15;

        // 5. Typography
        // Check if we need a new page
        if (yPos > pageHeight - 60) {
            doc.addPage();
            yPos = 20;
        }

        doc.setFontSize(16);
        doc.setTextColor(0, 0, 0);
        doc.text("Typography", 14, yPos);
        yPos += 8;

        const typographyData = [
            ["Header Font", result.typography.header],
            ["Body Font", result.typography.body],
            ["Sizing Scale", result.typography.sizing],
        ];

        autoTable(doc, {
            startY: yPos,
            body: typographyData,
            theme: 'grid',
            columnStyles: { 0: { fontStyle: 'bold', cellWidth: 40 } }
        });

        // @ts-ignore
        yPos = doc.lastAutoTable.finalY + 15;

        // 6. Reasoning
        if (yPos > pageHeight - 50) {
            doc.addPage();
            yPos = 20;
        }

        doc.setFontSize(16);
        doc.setTextColor(0, 0, 0);
        doc.text("Design Reasoning", 14, yPos);
        yPos += 8;

        doc.setFontSize(11);
        doc.setTextColor(60, 60, 60);
        const splitReason = doc.splitTextToSize(result.reasoning, pageWidth - 28);
        doc.text(splitReason, 14, yPos);

        // Footer - Branding (Moved to bottom)
        const totalPages = doc.getNumberOfPages();
        for (let i = 1; i <= totalPages; i++) {
            doc.setPage(i);
            doc.setFontSize(9);
            doc.setTextColor(150, 150, 150);
            doc.text("Generated by BNFN AI", pageWidth / 2, pageHeight - 10, { align: "center" });
        }

        doc.save(`${(result.businessName || "brand").toLowerCase().replace(/\s+/g, '-')}-brand-bible.pdf`);
    };

    return (
        <div className="flex flex-col gap-6 w-full max-w-4xl mx-auto animate-fade-in">
            <div className="flex flex-col md:flex-row justify-between items-start md:items-center gap-4">
                <div>
                    <h2 className="text-3xl font-bold bg-clip-text text-transparent bg-gradient-to-r from-primary to-secondary">
                        {result.businessName || "Your Brand Identity"}
                    </h2>
                    {result.description && (
                        <p className="text-default-500 mt-1 max-w-xl text-small">
                            {result.description}
                        </p>
                    )}
                </div>
                <Button
                    color="primary"
                    variant="shadow"
                    startContent={<DownloadIcon size={18} />}
                    onPress={downloadPDF}
                >
                    Download PDF
                </Button>
            </div>

            <div className="grid grid-cols-1 md:grid-cols-2 gap-6">
                {/* Theme & Concept */}
                <Card className="col-span-1 md:col-span-2 shadow-lg border border-default-200">
                    <CardHeader className="flex gap-3">
                        <div className="flex flex-col">
                            <p className="text-md font-bold">Theme Concept</p>
                        </div>
                    </CardHeader>
                    <Divider />
                    <CardBody>
                        <p className="text-default-600 leading-relaxed">{result.themeDescription}</p>
                    </CardBody>
                </Card>

                {/* Color Palette */}
                <Card className="shadow-md border border-default-200">
                    <CardHeader>
                        <p className="text-md font-bold">Color Palette</p>
                    </CardHeader>
                    <Divider />
                    <CardBody className="gap-3">
                        {Object.entries(result.colorPalette).map(([key, value]) => (
                            <div key={key} className="flex items-center justify-between p-2 rounded-lg bg-default-50">
                                <span className="capitalize text-small font-medium text-default-600">{key}</span>
                                <div className="flex items-center gap-3">
                                    <span className="text-xs text-default-400 font-mono uppercase">{value}</span>
                                    <div
                                        className="w-12 h-8 rounded-md shadow-sm border border-default-200"
                                        style={{ backgroundColor: value }}
                                    />
                                </div>
                            </div>
                        ))}
                    </CardBody>
                </Card>

                {/* Typography */}
                <Card className="shadow-md border border-default-200">
                    <CardHeader>
                        <p className="text-md font-bold">Typography</p>
                    </CardHeader>
                    <Divider />
                    <CardBody className="gap-4">
                        <div>
                            <p className="text-xs text-default-500 uppercase tracking-wider mb-1">Header Font</p>
                            <p className="text-large font-bold">{result.typography.header}</p>
                        </div>
                        <div>
                            <p className="text-xs text-default-500 uppercase tracking-wider mb-1">Body Font</p>
                            <p className="text-medium">{result.typography.body}</p>
                        </div>
                        <div>
                            <p className="text-xs text-default-500 uppercase tracking-wider mb-1">Sizing Scale</p>
                            <p className="text-small text-default-600 italic">{result.typography.sizing}</p>
                        </div>
                    </CardBody>
                </Card>

                {/* Reasoning */}
                <Card className="col-span-1 md:col-span-2 border border-primary/20 bg-primary/5">
                    <CardHeader>
                        <p className="text-md font-bold text-primary">Why this style?</p>
                    </CardHeader>
                    <CardBody>
                        <p className="text-default-700 text-small italic leading-relaxed">"{result.reasoning}"</p>
                    </CardBody>
                </Card>
            </div>

            <div className="flex justify-center mt-8 pb-10">
                <Button variant="flat" color="default" onPress={onReset}>
                    Create Another Brand
                </Button>
            </div>
        </div>
    );
}
